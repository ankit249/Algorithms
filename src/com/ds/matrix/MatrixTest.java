package com.ds.matrix;

public class MatrixTest {

	public static void main(String[] args) {
		char[][] m = {
					  {'A','B'},
					  {'C','D'}
					  };
		
		
		System.out.println(m[0][0]); // A
		System.out.println(m[0][1]); // B
		System.out.println(m[1][0]); // C
		System.out.println(m[1][1]); // D
	}
}
